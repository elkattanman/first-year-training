    private void add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_btnActionPerformed
        DefaultTableModel model = (DefaultTableModel) table_.getModel();
        String tmp = (String) pid_field.getSelectedItem();
        String s1 = mrp_field.getText();
        String s2 = Quan_field.getText();
        String s3 = disc_field.getText();
        if(s2.isEmpty()){
            s2="0";
            JOptionPane.showMessageDialog(null, "You should input Quantity ");
        }
        if(s3.isEmpty())
            s3="0.0";
        double mrp = Double.parseDouble(s1);
        int quan = Integer.parseInt(s2);
        double disc = (100.0 - (Double.parseDouble(s3))) / 100;
        double price = mrp * quan * disc;
        Connection con = null;
        Statement st1 = null;
        Statement st2 = null;
        ResultSet rs=null;
        String sql1="select * from `stock` Where id ='"+tmp+"'";
        String sql2="UPDATE `stock` SET `available` = `available` -'"+ quan +"' WHERE `id`='"+tmp+"'";
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Supermarket", "root", "");
            st1 = con.createStatement();
            st2 = con.createStatement();
            rs=st1.executeQuery(sql1);
            while(rs.next()){
                String s4=rs.getString(3);
                int ava=Integer.parseInt(s4);
                if(quan>ava){
                    JOptionPane.showMessageDialog(null, "Quantity Can't Exceed Available in stock ( "+ava+" left in stock)");
                }else{
                    st2.executeUpdate(sql2);
                    model.addRow(new Object[]{pid_field.getSelectedItem(), pname_field.getText(), mrp, quan, price});
                }
            }
            gettotal();
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_add_btnActionPerformed

