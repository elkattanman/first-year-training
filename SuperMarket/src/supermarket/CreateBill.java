    public void gettotal() {
        double sum = 0;
        for (int i = 0; i < table_.getRowCount(); i++) {
            sum = sum + Double.parseDouble(table_.getValueAt(i, 4).toString());
        }
        total_field.setText(Double.toString(sum));
    }
    
